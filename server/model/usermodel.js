import mongoose from 'mongoose';

const registerUserSchema = new mongoose.Schema({
    username: {
        type: String,
        required: true,
        unique: true,
        trim: true,
    },
    email: {
        type: String,
        required: true,
        unique: true,
        trim: true,
    },
    password: {
        type: String,
        required: true,
        trim: true,
        
    },
});

const registerUser = new mongoose.model('userdata', registerUserSchema);

export default registerUser;